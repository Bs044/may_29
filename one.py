number = input('Введите число от 0-9: ')
number = int(number)
if number == 1:
    print(f'у {number} этот спецсимвол: !')
elif number == 2:
    print(f'у {number} этот спецсимвол: @')
elif number == 3:
    print(f'у {number} этот спецсимвол: #')
elif number == 4:
    print(f'у {number} этот спецсимвол: $')
elif number == 5:
    print(f'у {number} этот спецсимвол: %')
elif number == 6:
    print(f'у {number} этот спецсимвол: ^')
elif number == 7:
    print(f'у {number} этот спецсимвол: &')
elif number == 8:
    print(f'у {number} этот спецсимвол: *')
elif number == 9:
    print(f'у {number} этот спецсимвол: (')
        
